import math # Imported math module for logarithm function

x = float(input("Enter number x: ")) # Input for base number
y = float(input("Enter number y: ")) # Input for exponent
print(f"{x} raised to the power {y} is {x ** y}") # Exponentiation
print(f"The log (base 2) of {x} is {math.log2(x)}") # Logarithm base 2

